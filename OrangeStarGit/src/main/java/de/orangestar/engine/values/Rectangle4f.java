package de.orangestar.engine.values;

public class Rectangle4f {

    public Rectangle4f() {
        
    }
    
    public Rectangle4f(int x1, int y1, int width1, int height1) {
        x = x1;
        y = y1;
        width = width1;
        height = height1;
    }
    
    public float x;
    public float y;
    public float width;
    public float height;
    
}
